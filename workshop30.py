# ********************************************
# **** เกมส์ทายตัวเลขที่มีค่าอยู่ที่ 1 - 100 กันเถอะ ****
# ********************************************
# ป้อนตัวเลขที่ต้องการทาย : 25
# คุณทายผิดตัวเลขที่ป้อนน้อยไป
# *********************************************
# ป้อนตัวเลขที่ต้องการทาย : 85
# คุณทายผิดตัวเลขที่ป้อนมากไป
# *********************************************
# ป้อนตัวเลขที่ต้องการทาย : 80
# คุณทายผิดตัวเลขที่ป้อนมากไป
# *********************************************
# ป้อนตัวเลขที่ต้องการทาย : 59
# ยินดีด้วยคุณทายถูก
# *********************************************

from random import randint

random_number = randint(1, 100)

print("========================================")
print(" โปรแกรมทายตัวเลข ") 
print("========================================")

while True:
        user_input = int(input("ลองเดาเลขดู : "))
        if user_input == random_number:
            print("ถูกต้องแล้วนะค้าบบบ ! ถูกเฉยยยย .")
            break
        elif user_input < random_number:
            print("ต่ำไปหน่อยยย. ลองอีกที.")
        else:
            print("สูงงงงไปหน่อย. ลองใหม่.")

print("========================================")
print("Thank you for playing  game.")