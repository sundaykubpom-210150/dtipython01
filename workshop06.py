#Operator ตัวดำเนินการ คือ เครืองหมาย
# 1. Arithemmetic Opt. เครื่องหมายคำนวณ
# + - * / **(exponential) %(mod) //(floor)
print(10+3)
print(10-3)
print(10*3)
print(10/3)
print(10%3)
print(10//3)
print(10**3)
print("++++++++++++++++")
# 2.  Comparison Opt. เครื่องหมายเปรียบเทียบ ผลลัพธ์ แค่ True / False 
# == , != , > , < , >= , <=
print(10 >= 25)   #False
print(999 < 5)    #False
print('Rayoug' >= 'Ranong')
# 3. Logical Opt. ตรรกะ
# not , and , or
# ข้อมูลที่จะใช้เครื่องมือ มีแค่ True/False (boolean) และผลลัพธ์ True/False (boolean)
print(not True)
print(not False)
print('++++++++++++++++')
print(True and True) #True
print(True and False) #False
print(False and True) #False
print(False and False) #False
print('++++++++++++++++')
print(True or True) #True
print(True or False) #True
print(False or True) #True
print(False or False) #False